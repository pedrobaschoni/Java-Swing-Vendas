    private void bParcelasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bParcelasActionPerformed
        int selectedRow = tabela.getSelectedRow();
        if (selectedRow > -1) {
            Venda venda = vendas.get(selectedRow);
            new ParcelasListView(null, true, venda)
                    .setVisible(true);
            
            bPesquisarActionPerformed(null);
        }


    }//GEN-LAST:event_bParcelasActionPerformed

