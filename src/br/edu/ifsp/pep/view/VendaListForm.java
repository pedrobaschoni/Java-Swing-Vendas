    private void bPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPesquisarActionPerformed
        if (dcDataInicio.getDate().before(dcDataFim.getDate())) {

            LocalDateTime localDateInicio
                    = LocalDateTime.ofInstant(
                            dcDataInicio.getDate().toInstant(),
                            ZoneId.systemDefault());

            LocalDateTime localDateFim
                    = LocalDateTime.ofInstant(
                            dcDataFim.getDate().toInstant(),
                            ZoneId.systemDefault());

            vendas = vendaDAO.buscarPorData(localDateInicio, localDateFim);

            atualizarTabela();

        } else {
            JOptionPane.showMessageDialog(
                    null,
                    "A Data de Início deve ser inferior a Data Fim.",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_bPesquisarActionPerformed

