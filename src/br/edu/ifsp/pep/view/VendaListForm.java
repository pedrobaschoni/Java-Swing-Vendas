    private void atualizarTabela() {

        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Venda venda : vendas) {
            modelo.addRow(new Object[]{
                venda.getId(),
                venda.getData(),
                venda.getStatus(),
                venda.getPessoa().getNome()
            });
        }
    }
