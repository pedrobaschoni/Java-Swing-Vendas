    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        
        int linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada > -1) {
            Pessoa pessoaSelecionada = pessoas.get(linhaSelecionada);
            
            if (!pessoaSelecionada.equals(PrincipalForm.getPessoaLogada())) {
                
                int resposta = JOptionPane.showConfirmDialog(
                        null, "Confirma a exclusÃ£o?",
                        "ConfirmaÃ§Ã£o",
                        JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    pessoaDAO.excluir(pessoaSelecionada);

                    //Atualiza a JTable.
                    //Evento de clique do botÃ£o pesquisar.
                    bPesquisarActionPerformed(null);
                }
            } else {
                JOptionPane.showMessageDialog(null,
                        "NÃ£o pode excluir a Pessoa Logada.",
                        "Aviso",
                        JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Selecione uma Pessoa.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_bExcluirActionPerformed

    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed
        PessoaCreateForm pessoaCreateForm = 
                new PessoaCreateForm(null, true);
        pessoaCreateForm.setVisible(true);
        
        if (pessoaCreateForm.getPessoa() != null) {
            try {
                pessoaDAO.inserir(pessoaCreateForm.getPessoa());
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
            //atualizar a JTable.
            bPesquisarActionPerformed(null);
        }
        
        
        
    }//GEN-LAST:event_bCadastrarActionPerformed

